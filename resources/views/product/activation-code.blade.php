@section('page_js')
    <script>
        document.getElementById('generateCodeBtn').addEventListener('click', function () {
            fetch('/generate-code', {
                method: 'POST',
                headers: {
                    'X-CSRF-TOKEN': document.querySelector('meta[name="csrf-token"]').content,
                    'Content-Type': 'application/json'
                },
                body: JSON.stringify({})
            })
            .then(response => response.json())
            .then(data => {
                if (data.data) {
                    toastr.success(`Your Activation Code: ${data.data.code}`);   
                    let newRow = `
                        <tr class="text-left"> 
                            <td>${data.data.id}</td>
                            <th>${data.data.code}</th>
                            <td>${data.data.generated_by.name}</td>
                            <td>${data.data.amount}</td>
                            <td>${data.data.admin_approval}</td>
                            <th>${data.data.status}</th>
                            <td>${data.data.used_by ?? 'N/A'}</td> 
                            <td>${data.data.updated_at ?? 'N/A'}</td>
                            <td>${data.data.created_at}</td>
                            <td>                   
                                <button class="btn btn-sm btn-light-danger rounded-0 delete-btn" 
                                    data-id="${data.data.id}" 
                                    data-toggle="modal" 
                                    data-target="#delete-confirmation-modal">
                                    <i class="icon-1x text-danger-50 flaticon-delete"></i>
                                </button>
                            </td>
                        </tr>
                    `;
                    document.querySelector('tbody').insertAdjacentHTML('afterbegin', newRow);
                    document.getElementById('currentPvBalance').innerText = data.new_balance;
                    $('#generate-activation-code').modal('hide');
                } 
            })
            .catch(error => console.error('Error:', error));
        });  
    </script>
    <script>
        document.addEventListener('DOMContentLoaded', function () {
            let deleteCodeId = null; 
            // When the delete button is clicked, set the ID
            document.querySelectorAll('.delete-btn').forEach(button => {
                button.addEventListener('click', function () {
                    deleteCodeId = this.getAttribute('data-id');
                    document.getElementById('deleteActivationCodeId').value = deleteCodeId;
                });
            }); 
            document.getElementById('confirmDeleteBtn').addEventListener('click', function () {
                if (!deleteCodeId) return;
    
                fetch(`/delete-code/${deleteCodeId}`, {
                    method: 'DELETE',
                    headers: {
                        'X-CSRF-TOKEN': document.querySelector('meta[name="csrf-token"]').content,
                        'Content-Type': 'application/json'
                    }
                })
                .then(response => response.json())
                .then(data => {
                    if (data.success) {
                        toastr.success('Activation code deleted successfully!');
                        document.querySelector(`button[data-id="${deleteCodeId}"]`).closest('tr').remove();
                        $('#delete-confirmation-modal').modal('hide');
                    } else {
                        toastr.error('Failed to delete activation code.');
                    }
                })
                .catch(error => console.error('Error:', error));
            });
        });
    </script>
    

