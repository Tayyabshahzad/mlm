@section('page_js')
    
    <script>
        document.addEventListener('DOMContentLoaded', function () {
            let deleteCodeId = null; 
            // When the delete button is clicked, set the ID
            document.querySelectorAll('.delete-btn').forEach(button => {
                button.addEventListener('click', function () {
                    deleteCodeId = this.getAttribute('data-id');
                    document.getElementById('deleteActivationCodeId').value = deleteCodeId;
                });
            }); 
            document.getElementById('confirmDeleteBtn').addEventListener('click', function () {
                if (!deleteCodeId) return;
    
                fetch(`/delete-code/${deleteCodeId}`, {
                    method: 'DELETE',
                    headers: {
                        'X-CSRF-TOKEN': document.querySelector('meta[name="csrf-token"]').content,
                        'Content-Type': 'application/json'
                    }
                })
                .then(response => response.json())
                .then(data => {
                    if (data.success) {
                        toastr.success('Activation code deleted successfully!');
                        document.querySelector(`button[data-id="${deleteCodeId}"]`).closest('tr').remove();
                        $('#delete-confirmation-modal').modal('hide');
                    } else {
                        toastr.error('Failed to delete activation code.');
                    }
                })
                .catch(error => console.error('Error:', error));
            });
        });

        $(document).ready(function () {
    $('.update-status-btn').click(function () {
        let id = $(this).data('id');
        let approvalStatus = $(this).data('approval-status'); // Fix attribute case sensitivity
        $('#activation-code-id').val(id);
        $('#new-status').val(approvalStatus);
    });

        $('#update-status-form').submit(function (e) {
            e.preventDefault();
            let id = $('#activation-code-id').val();
            let approvalStatus = $('#new-status').val();

            $.ajax({
                url: "{{ route('admin.activation-code.update-status') }}",
                method: "POST",
                data: {
                    _token: "{{ csrf_token() }}",
                    id: id,
                    admin_approval: approvalStatus
                },
                success: function (response) {
                    $('#status-update-modal').modal('hide');

                    // Display success message
                    toastr.success(response.message);

                    // Update status dynamically in the table without reload
                    $(`.status-label[data-id="${id}"]`).text(approvalStatus.toUpperCase()).removeClass().addClass(`status-label badge badge-${getBadgeClass(approvalStatus)}`);
                },
                error: function (xhr) {
                    let errorMessage = "Error updating status";
                    
                    if (xhr.responseJSON && xhr.responseJSON.message) {
                        errorMessage = xhr.responseJSON.message;
                    }

                    // Display error message
                    toastr.error(errorMessage);
                }
            });
        });

        function getBadgeClass(status) {
            switch (status) {
                case 'approved': return 'success';
                case 'rejected': return 'danger';
                case 'pending': return 'warning';
                default: return 'secondary';
            }
        }
    });

    </script>
    

